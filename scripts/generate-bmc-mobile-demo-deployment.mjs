export const buildBmcMobileDemoDeploymentArtifacts = async () => {
  const [categories, governance, governanceChecksum, routing, routingVerification] =
    await Promise.all(Object.values(sourcePaths).map((path) => readFile(path, 'utf8')));

  const categoryBody = categories
    .slice(assertMarker(categories, 'insert into routing.issue_domains (', 'category seed'))
    .trim();
  const governanceCore = sliceBetween(
    governance,
    'insert into governance.reference_sources (',
    'insert into governance.jurisdiction_boundary_versions (',
    'BMC governance seed',
  );
  const boundaryBody = sliceBetween(
    governance,
    'insert into governance.jurisdiction_boundary_versions (',
    'insert into governance.ward_administrative_zone_membership_versions (',
    'BMC governance seed',
  );
  const crosswalkStart = assertMarker(
    governance,
    'insert into governance.ward_administrative_zone_membership_versions (',
    'BMC governance seed',
  );
  const governanceCommit = governance.lastIndexOf('commit;');
  const crosswalkBody = governance.slice(crosswalkStart, governanceCommit).trim();
  const checksumBody = transactionBody(governanceChecksum, 'BMC checksum seed');

  const routingBody = transactionBody(routing, 'BMC routing seed');
  const routingPreflight = sliceBetween(
    routingBody,
    'do $bmc_internal_routing_preflight$',
    'update routing.issue_domains',
    'BMC routing seed',
  );
  const routingMutations = sliceBetween(
    routingBody,
    'update routing.issue_domains',
    'do $bmc_internal_routing_postflight$',
    'BMC routing seed',
  );
  const routingPostflight = routingBody
    .slice(assertMarker(routingBody, 'do $bmc_internal_routing_postflight$', 'BMC routing seed'))
    .trim();
  const routingVerificationBody = transactionBody(
    routingVerification,
    'BMC routing verification seed',
  );

  return new Map([
    [outputPaths.baseline, renderPart1(categoryBody, governanceCore)],
    [outputPaths.boundaries, renderPart2(boundaryBody)],
    [outputPaths.crosswalk, renderPart3(crosswalkBody, checksumBody)],
    [
      outputPaths.routing,
      renderPart4(routingPreflight, routingMutations, routingPostflight, routingVerificationBody),
    ],
    [outputPaths.readme, renderReadme()],
  ]);
};

const checkArtifacts = async (artifacts) => {
  const stale = [];
  for (const [path, expected] of artifacts) {
    let actual;
    try {
      actual = await readFile(path, 'utf8');
    } catch {
      stale.push(`${relative(repositoryRoot, path)} is missing`);
      continue;
    }
    if (actual !== expected) {
      stale.push(`${relative(repositoryRoot, path)} is stale`);
    }
  }
  if (stale.length > 0) {
    throw new Error(
      `BMC mobile demo deployment artifacts are not current:\n- ${stale.join('\n- ')}`,
    );
  }
};

const writeArtifacts = async (artifacts) => {
  for (const [path, contents] of artifacts) {
    await mkdir(dirname(path), { recursive: true });
    await writeFile(path, contents, 'utf8');
  }
};

const main = async () => {
  const checkOnly = process.argv.includes('--check');
  const artifacts = await buildBmcMobileDemoDeploymentArtifacts();
  if (checkOnly) {
    await checkArtifacts(artifacts);
  } else {
    await writeArtifacts(artifacts);
  }
  process.stdout.write(
    `${checkOnly ? 'Checked current' : 'Generated'} BMC mobile demo SQL Editor deployment bundle.\n`,
  );
};

if (resolve(process.argv[1] ?? '') === fileURLToPath(import.meta.url)) {
  await main();
}
