const assertMarker = (source, marker, sourceName) => {
  const index = source.indexOf(marker);
  if (index === -1) {
    throw new Error(`Expected marker ${JSON.stringify(marker)} in ${sourceName}.`);
  }
  return index;
};

const sliceBetween = (source, startMarker, endMarker, sourceName) => {
  const start = assertMarker(source, startMarker, sourceName);
  const end = assertMarker(source, endMarker, sourceName);
  if (end <= start) {
    throw new Error(`Invalid marker order in ${sourceName}.`);
  }
  return source.slice(start, end).trim();
};

const transactionBody = (source, sourceName) => {
  const start = assertMarker(source, 'begin;', sourceName) + 'begin;'.length;
  const end = source.lastIndexOf('commit;');
  if (end === -1 || end <= start) {
    throw new Error(`Expected a complete transaction in ${sourceName}.`);
  }
  return source.slice(start, end).trim();
};

