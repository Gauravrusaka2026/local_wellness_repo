import { createHash } from 'node:crypto';
import { mkdir, readFile, writeFile } from 'node:fs/promises';
import { dirname, join, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const repositoryRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const sourcePaths = {
  routingMigration: join(
    repositoryRoot,
    'supabase/migrations/20260720100000_v1_simple_ward_routing.sql',
  ),
  emailProvenanceMigration: join(
    repositoryRoot,
    'supabase/migrations/20260720103000_v1_ward_email_provenance.sql',
  ),
  seed: join(repositoryRoot, 'supabase/seed/54_bmc_v1_ward_issue_contacts.generated.sql'),
};
const outputPath = join(repositoryRoot, 'supabase/deploy/v1-simple-ward-routing.sql');
const checkOnly = process.argv.includes('--check');

const repositoryPath = (path) => relative(repositoryRoot, path).replaceAll('\\', '/');
const sha256 = (value) => createHash('sha256').update(value).digest('hex');
const ensureTrailingNewline = (value) => (value.endsWith('\n') ? value : `${value}\n`);

const routingMigrationStartMarker = '-- BEGIN EXACT SOURCE: routing migration\n';
const routingMigrationEndMarker = '-- END EXACT SOURCE: routing migration\n';
const emailProvenanceMigrationStartMarker = '-- BEGIN EXACT SOURCE: email provenance migration\n';
const emailProvenanceMigrationEndMarker = '-- END EXACT SOURCE: email provenance migration\n';
const seedStartMarker = '-- BEGIN EXACT SOURCE: seed\n';
const seedEndMarker = '-- END EXACT SOURCE: seed\n';

const extractExactSource = (deployment, startMarker, endMarker, label) => {
  const start = deployment.indexOf(startMarker);
  const end = deployment.indexOf(endMarker);

  if (start === -1 || end === -1 || end <= start) {
    throw new Error(`Unable to locate the exact ${label} payload in the deployment artifact.`);
  }

  return deployment.slice(start + startMarker.length, end);
};

const renderDeployment = ({ routingMigration, emailProvenanceMigration, seed }) => {
  const routingMigrationSha256 = sha256(routingMigration);
  const emailProvenanceMigrationSha256 = sha256(emailProvenanceMigration);
  const seedSha256 = sha256(seed);
  const orderedPayloadSha256 = sha256(`${routingMigration}${emailProvenanceMigration}${seed}`);

  return `-- Local Wellness V1 simple ward-routing SQL Editor deployment
-- Generated by scripts/generate-v1-simple-ward-routing-deployment.mjs. Do not edit manually.
--
-- Prerequisites:
--   1. Apply the repository's existing Supabase schema through the migration immediately
--      preceding 20260720100000_v1_simple_ward_routing.sql.
--   2. Load the existing Maharashtra/BMC governance baseline, including the BMC authority,
--      local body, 26 operational wards, ward geometry/crosswalks, departments, and roles.
--   3. Run this complete file once in the hosted Supabase SQL Editor.
--
-- This file intentionally has no governance-source snapshot registry gate. The immutable
-- contact ZIP checksums are validated by the generated seed itself. The migrations are applied
-- first, followed byte-for-byte by the generated 26-ward x 12-category contact seed.
-- Routing migration SHA-256: ${routingMigrationSha256}
-- Email-provenance migration SHA-256: ${emailProvenanceMigrationSha256}
-- Seed SHA-256: ${seedSha256}
-- Ordered source payload SHA-256: ${orderedPayloadSha256}

${routingMigrationStartMarker}${routingMigration}${routingMigrationEndMarker}
${emailProvenanceMigrationStartMarker}${emailProvenanceMigration}${emailProvenanceMigrationEndMarker}
${seedStartMarker}${seed}${seedEndMarker}`;
};

const main = async () => {
  const routingMigration = ensureTrailingNewline(
    await readFile(sourcePaths.routingMigration, 'utf8'),
  );
  const emailProvenanceMigration = ensureTrailingNewline(
    await readFile(sourcePaths.emailProvenanceMigration, 'utf8'),
  );
  const seed = ensureTrailingNewline(await readFile(sourcePaths.seed, 'utf8'));

  const obsoleteSnapshotGate = 'BMC ward-directory source endpoint/snapshot is missing';
  if (
    routingMigration.includes(obsoleteSnapshotGate) ||
    emailProvenanceMigration.includes(obsoleteSnapshotGate) ||
    seed.includes(obsoleteSnapshotGate)
  ) {
    throw new Error('The V1 deployment sources contain the obsolete BMC snapshot gate.');
  }

  const expected = renderDeployment({ routingMigration, emailProvenanceMigration, seed });

  if (checkOnly) {
    const current = await readFile(outputPath, 'utf8').catch(() => null);
    if (current !== expected) {
      throw new Error(
        `${repositoryPath(outputPath)} is missing or stale. Run ` +
          '`pnpm governance:bmc:v1-routing:deploy:generate`.',
      );
    }

    const embeddedRoutingMigration = extractExactSource(
      current,
      routingMigrationStartMarker,
      routingMigrationEndMarker,
      'routing migration',
    );
    const embeddedEmailProvenanceMigration = extractExactSource(
      current,
      emailProvenanceMigrationStartMarker,
      emailProvenanceMigrationEndMarker,
      'email provenance migration',
    );
    const embeddedSeed = extractExactSource(current, seedStartMarker, seedEndMarker, 'seed');

    if (
      embeddedRoutingMigration !== routingMigration ||
      embeddedEmailProvenanceMigration !== emailProvenanceMigration ||
      embeddedSeed !== seed
    ) {
      throw new Error('The deployment artifact does not preserve its source SQL byte-for-byte.');
    }

    if (current.includes(obsoleteSnapshotGate)) {
      throw new Error('The deployment artifact contains the obsolete BMC snapshot gate.');
    }
  } else {
    await mkdir(dirname(outputPath), { recursive: true });
    await writeFile(outputPath, expected, 'utf8');
  }

  process.stdout.write(
    `${checkOnly ? 'Checked' : 'Generated'} ${repositoryPath(outputPath)} ` +
      `(${Buffer.byteLength(expected)} bytes; ordered payload SHA-256 ${sha256(
        `${routingMigration}${emailProvenanceMigration}${seed}`,
      )}).\n`,
  );
};

await main();
